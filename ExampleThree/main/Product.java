package main;

public abstract class Product {

	/**
	 * In this example we show how Java OOP really
	 * can work using Inheritance, abstraction, and
	 * interfaces.
	 */
	
	/**
	 * We first produce a product class that
	 * contains a product number and manufacturers number.
	 * This allows us produce any type of sub products
	 */
	protected String productNumber;
	protected String manufactureNumber;
}
